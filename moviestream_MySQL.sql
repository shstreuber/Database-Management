/*============================================================*/
/* Table: CUSTOMER_ACCOUNT                                    */
/*============================================================*/
CREATE TABLE CUSTOMER_ACCOUNT (
CUSTOMER_ACCOUNT_ID  INTEGER              NOT NULL,
CUSTOMER_HOLD_INDIC  CHAR(1)  DEFAULT 'N' NOT NULL  
      CHECK (CUSTOMER_HOLD_INDIC IN ('Y','N')),
DATE_ENROLLED        DATE                 NOT NULL,
DATE_TERMINATED      DATE                 NULL,
CUSTOMER_DEPOSIT_AMOUNT NUMERIC(5,2)      NULL,
CREDIT_CARD_ON_FILE_INDIC CHAR(1)         NOT NULL  
      CHECK (CREDIT_CARD_ON_FILE_INDIC IN ('Y','N')),
CHILD_RENTAL_ALLOWED_INDIC CHAR(1)        NOT NULL  
      CHECK (CHILD_RENTAL_ALLOWED_INDIC IN ('Y','N')),
PRIMARY KEY (CUSTOMER_ACCOUNT_ID)
);

/*============================================================*/
/* Table: LANGUAGE                                            */
/*============================================================*/
CREATE TABLE LANGUAGE (
LANGUAGE_CODE        CHAR(2)              NOT NULL,
LANGUAGE_NAME        VARCHAR(40)          NOT NULL,
PRIMARY KEY (LANGUAGE_CODE)
);

/*============================================================*/
/* Table: MOVIE_GENRE                                         */
/*============================================================*/
CREATE TABLE MOVIE_GENRE (
MOVIE_GENRE_CODE     CHAR(5)              NOT NULL,
MOVIE_GENRE_DESCRIPTION VARCHAR(100)      NOT NULL,
PRIMARY KEY (MOVIE_GENRE_CODE)
);

/*============================================================*/
/* Table: MPAA_RATING                                         */
/*============================================================*/
CREATE TABLE MPAA_RATING (
MPAA_RATING_CODE     CHAR(5)              NOT NULL,
MPAA_RATING_DESCRIPTION VARCHAR(100)      NOT NULL,
PRIMARY KEY (MPAA_RATING_CODE)
);

/*============================================================*/
/* Table: PERSON                                              */
/*============================================================*/
CREATE TABLE PERSON (
PERSON_ID            INTEGER              NOT NULL,
PERSON_GIVEN_NAME    VARCHAR(25)          NOT NULL,
PERSON_MIDDLE_NAME   VARCHAR(25)          NULL,
PERSON_FAMILY_NAME   VARCHAR(25)          NOT NULL,
PERSON_ADDRESS_1     VARCHAR(100)         NULL,
PERSON_ADDRESS_2     VARCHAR(100)         NULL,
PERSON_ADDRESS_CITY  VARCHAR(40)          NULL,
PERSON_ADDRESS_STATE_PROV CHAR(2)         NULL,
PERSON_ADDRESS_POSTAL_CODE VARCHAR(10)    NULL,
PERSON_ADDRESS_COUNTRY CHAR(2)            NULL,
PERSON_PHONE         VARCHAR(15)          NULL,
BIRTH_DATE           DATE                 NULL,
DEATH_DATE           DATE                 NULL,
PRIMARY KEY (PERSON_ID)
);

/*============================================================*/
/* Table: MOVIE                                               */
/*============================================================*/
CREATE TABLE MOVIE (
MOVIE_ID             INTEGER              NOT NULL,
MOVIE_GENRE_CODE     CHAR(5)              NOT NULL,
MPAA_RATING_CODE     CHAR(5)              NOT NULL,
MOVIE_TITLE          VARCHAR(100)         NOT NULL,
RENTAL_FEE_SD     NUMERIC(5,2)         NULL,
RENTAL_FEE_HD     NUMERIC(5,2)         NULL,
YEAR_PRODUCED        CHAR(4)              NULL,
PRIMARY KEY (MOVIE_ID),
FOREIGN KEY (MOVIE_GENRE_CODE)
      REFERENCES MOVIE_GENRE (MOVIE_GENRE_CODE),
FOREIGN KEY (MPAA_RATING_CODE)
      REFERENCES MPAA_RATING (MPAA_RATING_CODE)
);

/*============================================================*/
/* Index: IX_MOVIE_GENRE_MOVIE                                */
/*============================================================*/
CREATE INDEX IX_MOVIE_GENRE_MOVIE 
    ON MOVIE (MOVIE_GENRE_CODE ASC);

/*============================================================*/
/* Index: IX_MOVIE_TITLE                                      */
/*============================================================*/
CREATE INDEX IX_MOVIE_TITLE 
    ON MOVIE (MOVIE_TITLE ASC);

/*============================================================*/
/* Table: MOVIE_STREAM                                          */
/*============================================================*/
CREATE TABLE MOVIE_STREAM (
MOVIE_ID             INTEGER              NOT NULL,
STREAM_ID          INTEGER              NOT NULL,
DATE_ACQUIRED        DATE                 NOT NULL,
DATE_DROPPED            DATE                 NULL,
MEDIA_FORMAT         CHAR(2)              NOT NULL  
      CHECK (MEDIA_FORMAT IN ('SD','HD')),
PRIMARY KEY (MOVIE_ID, STREAM_ID),
FOREIGN KEY (MOVIE_ID)
      REFERENCES MOVIE (MOVIE_ID)
);

/*============================================================*/
/* Table: MOVIE_LANGUAGE                                      */
/*============================================================*/
CREATE TABLE MOVIE_LANGUAGE (
MOVIE_ID             INTEGER              NOT NULL,
LANGUAGE_CODE        CHAR(2)              NOT NULL,
PRIMARY KEY (MOVIE_ID, LANGUAGE_CODE),
FOREIGN KEY (LANGUAGE_CODE)
      REFERENCES LANGUAGE (LANGUAGE_CODE),
FOREIGN KEY (MOVIE_ID)
      REFERENCES MOVIE (MOVIE_ID)
);

/*============================================================*/
/* Index: IX_LANG_MOVIE_LANG                                  */
/*============================================================*/
CREATE INDEX IX_LANG_MOVIE_LANG 
    ON MOVIE_LANGUAGE (LANGUAGE_CODE ASC);

/*============================================================*/
/* Table: EMPLOYEE                                            */
/*============================================================*/
CREATE TABLE EMPLOYEE (
PERSON_ID            INTEGER              NOT NULL,
SUPERVISOR_PERSON_ID INTEGER              NULL,
EMPLOYEE_TAX_ID      VARCHAR(15)          NOT NULL,
EMPLOYEE_JOB_CATEGORY CHAR(1)             NOT NULL  
      CHECK (EMPLOYEE_JOB_CATEGORY IN ('M','C','O')),
EMPLOYEE_HOURLY_RATE NUMERIC(5,2)         NOT NULL,
HIRE_DATE            DATE                 NOT NULL,
TERMINATION_DATE     DATE                 NULL,
PRIMARY KEY (PERSON_ID),
FOREIGN KEY (PERSON_ID)
      REFERENCES PERSON (PERSON_ID),
FOREIGN KEY (SUPERVISOR_PERSON_ID)
      REFERENCES EMPLOYEE (PERSON_ID)
);

/*============================================================*/
/* Index: IX_SUPERVISOR_PERSON_ID                             */
/*============================================================*/
CREATE INDEX IX_SUPERVISOR_PERSON_ID 
    ON EMPLOYEE (SUPERVISOR_PERSON_ID ASC);

/*============================================================*/
/* Table: CUSTOMER_ACCOUNT_PERSON                             */
/*============================================================*/
CREATE TABLE CUSTOMER_ACCOUNT_PERSON (
CUSTOMER_ACCOUNT_ID  INTEGER              NOT NULL,
PERSON_ID            INTEGER              NOT NULL,
PRIMARY KEY (CUSTOMER_ACCOUNT_ID, PERSON_ID),
FOREIGN KEY (PERSON_ID)
      REFERENCES PERSON (PERSON_ID),
FOREIGN KEY (CUSTOMER_ACCOUNT_ID)
      REFERENCES CUSTOMER_ACCOUNT (CUSTOMER_ACCOUNT_ID)
);

/*============================================================*/
/* Index: IX_PERSON_CUSTOMER_ACCT_PERSON                      */
/*============================================================*/
CREATE INDEX IX_PERSON_CUSTOMER_ACCT_PERSON 
    ON CUSTOMER_ACCOUNT_PERSON (PERSON_ID ASC);

/*============================================================*/
/* Table: CUSTOMER_TRANSACTION                                */
/*============================================================*/
CREATE TABLE CUSTOMER_TRANSACTION (
TRANSACTION_ID       INTEGER              NOT NULL,
CUSTOMER_ACCOUNT_ID  INTEGER              NOT NULL,
EMPLOYEE_PERSON_ID   INTEGER              NOT NULL,
TRANSACTION_DATE     DATE                 NOT NULL,
SALES_TAX            NUMERIC(5,2)         NOT NULL,
PRIMARY KEY (TRANSACTION_ID),
FOREIGN KEY (EMPLOYEE_PERSON_ID)
      REFERENCES EMPLOYEE (PERSON_ID),
FOREIGN KEY (CUSTOMER_ACCOUNT_ID)
      REFERENCES CUSTOMER_ACCOUNT (CUSTOMER_ACCOUNT_ID)
);

/*============================================================*/
/* Index: IX_EMPLOYEE_CUSTOMER_TRANS                          */
/*============================================================*/
CREATE INDEX IX_EMPLOYEE_CUSTOMER_TRANS 
    ON CUSTOMER_TRANSACTION (EMPLOYEE_PERSON_ID ASC);

/*============================================================*/
/* Index: IX_CUST_ACCT_CUST_TRANS                             */
/*============================================================*/
CREATE INDEX IX_CUST_ACCT_CUST_TRANS 
    ON CUSTOMER_TRANSACTION (CUSTOMER_ACCOUNT_ID ASC);

/*============================================================*/
/* Table: MOVIE_RENTAL                                        */
/*============================================================*/
CREATE TABLE MOVIE_RENTAL (
MOVIE_ID             INTEGER              NOT NULL,
STREAM_ID          INTEGER              NOT NULL,
TRANSACTION_ID       INTEGER              NOT NULL,
DUE_DATE             DATE                 NULL,
RENTAL_FEE           NUMERIC(7,2)         NULL,
UPGRADE_FEE_SDHD     NUMERIC(5,2)         NULL,
CHECKIN_DATE        DATE                 NULL,
PRIMARY KEY (MOVIE_ID, STREAM_ID, TRANSACTION_ID),
FOREIGN KEY (MOVIE_ID, STREAM_ID)
      REFERENCES MOVIE_STREAM (MOVIE_ID, STREAM_ID),
FOREIGN KEY (TRANSACTION_ID)
      REFERENCES CUSTOMER_TRANSACTION (TRANSACTION_ID)
);

/*============================================================*/
/* Index: IX_MOVIE_CUST_TRANS                                 */
/*============================================================*/
CREATE INDEX IX_MOVIE_CUST_TRANS 
    ON MOVIE_RENTAL (TRANSACTION_ID ASC);

